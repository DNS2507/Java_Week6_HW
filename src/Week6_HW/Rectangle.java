package Week6_HW;
//Write a Java program to print the area and perimeter of a rectangle.


public class Rectangle {
    public static void main(String[] args) {
        double height=8.5d, width=5.5d; //Instance Variable
        System.out.println("Area is " +width +"*" +height +"=" +(height*width));
        System.out.println("Perameter is 2 *" +"(5.5+8.5) =" +(2*(width+height)));
}
}
